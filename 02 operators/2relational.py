# Relational operators and comparison operators are the same thing.
#    Relational → shows relationship between values
#    Comparison → compares two values
#                                               Both mean: “Compare two values and return True or False.”

print(11==4)
print(11>4)
print(11<4)
print(11>=4)
print(11<=4)
print(11!=4)

